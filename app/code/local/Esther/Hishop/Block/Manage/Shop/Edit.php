class Esther_Hishop_Block_Manage_Shop_Edit extends Mage_Adminhtml_Block_Widget_Form_Container
{
    public function __construct()
    {
        parent::__construct();
                 
        $this->_objectId = 'id';
        $this->_blockGroup = 'hishop';
        $this->_controller = 'manage_shop';
        
        $this->_updateButton('save', 'label', Mage::helper('hishop')->__('Save Shop'));
        $this->_updateButton('delete', 'label', Mage::helper('hishop')->__('Delete Shop'));
		
        $this->_addButton('saveandcontinue', array(
            'label'     => Mage::helper('adminhtml')->__('Save And Continue Edit'),
            'onclick'   => 'saveAndContinueEdit()',
            'class'     => 'save',
        ), -100);

        $this->_formScripts[] = "
            function toggleEditor() {
                if (tinyMCE.getInstanceById('blog_content') == null) {
                    tinyMCE.execCommand('mceAddControl', false, 'post_content');
                } else {
                    tinyMCE.execCommand('mceRemoveControl', false, 'post_content');
                }
            }

            function saveAndContinueEdit(){
                editForm.submit($('edit_form').action+'back/edit/');
            }
        ";
    }

    public function getHeaderText()
    {
        if( Mage::registry('shop_data') && Mage::registry('shop_data')->getId() ) {
            return Mage::helper('hishop')->__("Edit Shop '%s'", $this->htmlEscape(Mage::registry('shop_data')->getTitle()));
        } else {
            return Mage::helper('hishop')->__('Add Shop');
        }
    }
}
